package com.github.katavasija.bricklink;

public class ItemStatsIndicators {
	private String timesSold;
	private String totalQty;
	private String minPrice;
	private String avgPrice;
	private String qtyAvgPrice;
	private String maxPrice;

	public String getTimesSold() {
		return this.timesSold;
	}

	public void setTimesSold(String timesSold) {
		this.timesSold = timesSold;
	}

	public String getTotalQty() {
		return this.totalQty;
	}

	public void setTotalQty(String totalQty) {
		this.totalQty = totalQty;
	}

	public String getMinPrice() {
		return this.minPrice;
	}

	public void setMinPrice(String minPrice) {
		this.minPrice = minPrice;
	}

	public String getAvgPrice() {
		return this.avgPrice;
	}

	public void setAvgPrice(String avgPrice) {
		this.avgPrice = avgPrice;
	}

	public String getQtyAvgPrice() {
		return this.qtyAvgPrice;
	}

	public void setQtyAvgPrice(String qtyAvgPrice) {
		this.qtyAvgPrice = qtyAvgPrice;
	}

	public String getMaxPrice() {
		return this.maxPrice;
	}

	public void setMaxPrice(String maxPrice) {
		this.maxPrice = maxPrice;
	}

}
